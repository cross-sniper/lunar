/*
** Main body of stand-alone interpreter (to be called in protected mode).
** Reads the options and handles them all.
*/
static int pmain (luna_State *L) {
  int argc = (int)luna_tointeger(L, 1);
  char **argv = (char **)luna_touserdata(L, 2);
  int script;
  int args = collectargs(argv, &script);
  int optlim = (script > 0) ? script : argc; /* first argv not an option */
  lunaL_checkversion(L);  /* check that interpreter has correct version */
  if (args == has_error) {  /* bad arg? */
    print_usage(argv[script]);  /* 'script' has index of bad arg. */
    return 0;
  }
  if (args & has_v)  /* option '-v'? */
    print_version();
  if (args & has_E) {  /* option '-E'? */
    luna_pushboolean(L, 1);  /* signal for libraries to ignore env. vars. */
    luna_setfield(L, LUNA_REGISTRYINDEX, "LUNA_NOENV");
  }
  lunaL_openlibs(L);  /* open standard libraries */
  createargtable(L, argv, argc, script);  /* create table 'arg' */
  luna_gc(L, LUNA_GCRESTART);  /* start GC... */
  luna_gc(L, LUNA_GCGEN, 0, 0);  /* ...in generational mode */
  if (!(args & has_E)) {  /* no option '-E'? */
    if (handle_luainit(L) != LUNA_OK)  /* run LUNA_INIT */
      return 0;  /* error running LUNA_INIT */
  }
  if (!runargs(L, argv, optlim))  /* execute arguments -e and -l */
    return 0;  /* something failed */
  if (script > 0) {  /* execute main script (if there is one) */
    if (handle_script(L, argv + script) != LUNA_OK)
      return 0;  /* interrupt in case of error */
  }
  if (args & has_i)  /* -i option? */
    doREPL(L);  /* do read-eval-print loop */
  else if (script < 1 && !(args & (has_e | has_v))) { /* no active option? */
    if (luna_stdin_is_tty()) {  /* running in interactive mode? */
      print_version();
      doREPL(L);  /* do read-eval-print loop */
    }
    else dofile(L, NULL);  /* executes stdin as a file */
  }
  luna_pushboolean(L, 1);  /* signal no errors */
  return 1;
}


int main (int argc, char **argv) {
  int status, result;
  luna_State *L = lunaL_newstate();  /* create state */
  if (L == NULL) {
    l_message(argv[0], "cannot create state: not enough memory");
    return EXIT_FAILURE;
  }

  luna_gc(L, LUNA_GCSTOP);  /* stop GC while building state */
  luna_pushcfunction(L, &pmain);  /* to call 'pmain' in protected mode */
  luna_pushinteger(L, argc);  /* 1st argument */
  luna_pushlightuserdata(L, argv); /* 2nd argument */
  status = luna_pcall(L, 2, 1, 0);  /* do the call */
  result = luna_toboolean(L, -1);  /* get result */
  report(L, status);
  luna_close(L);
  return (result && status == LUNA_OK) ? EXIT_SUCCESS : EXIT_FAILURE;
}

